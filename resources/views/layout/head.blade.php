        function fetchInternetSpeed() {
            // Fetch the internet speed from the server using AJAX
            fetch('/get-speed')
                .then(response => response.json())
                .then(data => {
                    // Update the internet speed on the page
                    document.getElementById('internetSpeed').textContent = data.internet_speed;
                })
                .catch(error => console.error('Error fetching internet speed:', error));
        }

        // Update the internet speed every 1 second
        setInterval(fetchInternetSpeed, 1000);

        // Call the function once to display the initial internet speed
        fetchInternetSpeed();
